public class Operators {

 public static void main(String[] args) {
  
  // expression =     operands & operators
  // operands =  values, variables, numbers, quantity
  // operators = + - * / % 
    
	 double Balls=10;
	 
	 Balls++;
	 System.out.println(Balls);
	 
	 Balls=Balls*10;
	 System.out.println(Balls);
	 
	 Balls=Balls/2;
	 System.out.println(Balls);
	 
	 Balls=Balls%3;
	 System.out.println(Balls);
	 
	 Balls=Balls*10;
	 Balls=(double)Balls/3;
	 
	 System.out.println(Balls);
	 
	 
	 
 } 
}