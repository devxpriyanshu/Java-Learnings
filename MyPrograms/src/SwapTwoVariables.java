
public class SwapTwoVariables {

	public static void main(String[] args) {
		
		int x,y,temp;
		
		//let
		x=20;
		y=100;
		
		System.out.println("Numbers before swaping: \n x="+x + "\n y="+y);
		
		temp=x;
		x=y;
		y=temp;
		
		System.out.println("Numbers after swaping: \n x="+x + "\n y="+y);

	}

}
